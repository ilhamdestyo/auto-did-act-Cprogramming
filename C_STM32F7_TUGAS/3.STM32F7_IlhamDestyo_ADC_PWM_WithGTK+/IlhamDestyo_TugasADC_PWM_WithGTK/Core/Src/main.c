/**
  * @brief  Function implementing the ADC_Task thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartADC */
void StartADC(void const * argument)
{
  /* USER CODE BEGIN 5 */
	ret = HAL_ADC_Start(&hadc1);
	if(ret != HAL_OK){
		Error_Handler();
	}

	ret = HAL_ADC_PollForConversion(&hadc1, 20);
	if(ret != HAL_OK){
		Error_Handler();
	}

	HAL_Delay(100);


  /* Infinite loop */
  for(;;)
  {
	  sConfigPrivate.Rank = ADC_REGULAR_RANK_1;	//set ADC regular conversion rank 1
	  sConfigPrivate.Channel = ADC_CHANNEL_0;	//set ADC Channel 0
	  HAL_ADC_ConfigChannel(&hadc1, &sConfigPrivate);	//set configurations adc1 rank and channel configiration structur
	  HAL_ADC_Start(&hadc1);//Start ADC Enable
	  HAL_ADC_PollForConversion(&hadc1,1000);
	  adcValue = HAL_ADC_GetValue(&hadc1);//read ADC Value
	  adc = IntToStr(adcValue); //ADC convertion from integer to string
	  strcpy(Tx_data , adc);	// Copy ADC string to Tx_Data
	  HAL_ADC_Stop(&hadc1);	// Stop and Disable ADC
	  //transmit data uart from ADC
	  HAL_UART_Transmit(&huart1, (uint8_t *)Tx_data, sizeof(Tx_data), 1000);

    osDelay(500);	// delay 500ms
  }
  /* USER CODE END 5 */
}

