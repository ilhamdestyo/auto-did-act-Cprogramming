/**
* @brief Function implementing the PWM_Task thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartPWM */
void StartPWM(void const * argument)
{
  /* USER CODE BEGIN StartPWM */
  /* Infinite loop */
  for(;;)
  {
	  // Receive data from uart
	  HAL_UART_Receive(&huart1, (uint8_t *)&Rx_data, sizeof(Rx_data), 1000);
	  memset(buffer, '\0', 64);	// set clear buffer
	  memcpy(buffer, Rx_data, 64);	// copy data to buffer

	  if(buffer[0]=='0' && buffer[1]=='\0'){ // jika buffer = 0
		  PWM = 1; // set value PWM = 1 to duty cycle 1%
	  }
	  else if(buffer[0]=='1' && buffer[1]=='0' && buffer[2]=='\0'){ // jika buffer = 10
		  PWM = 100; // set value PWM = 100 to duty cycle 10%
	  }
	  else if(buffer[0]=='2' && buffer[1]=='0' && buffer[2]=='\0'){ // jika buffer = 20
		  PWM = 200; // set value PWM = 200 to duty cycle 20%
	  }
	  else if(buffer[0]=='3' && buffer[1]=='0' && buffer[2]=='\0'){ // jika buffer = 30
		  PWM = 300; // set value PWM = 300 to duty cycle 30%
	  }
	  else if(buffer[0]=='4' && buffer[1]=='0' && buffer[2]=='\0'){ // jika buffer = 40
		  PWM = 400; // set value PWM = 400 to duty cycle 40%
	  }
	  else if(buffer[0]=='5' && buffer[1]=='0' && buffer[2]=='\0'){ // jika buffer = 50
		  PWM = 500; // set value PWM = 500 to duty cycle 50%
	  }
	  else if(buffer[0]=='6' && buffer[1]=='0' && buffer[2]=='\0'){ // jika buffer = 60
		  PWM = 600; // set value PWM = 600 to duty cycle 60%
	  }
	  else if(buffer[0]=='7' && buffer[1]=='0' && buffer[2]=='\0'){ // jika buffer = 70
		  PWM = 700; // set value PWM = 700 to duty cycle 70%
	  }
	  else if(buffer[0]=='8' && buffer[1]=='0' && buffer[2]=='\0'){ // jika buffer = 80
		  PWM = 800; // set value PWM = 800 to duty cycle 80%
	  }
	  else if(buffer[0]=='9' && buffer[1]=='0' && buffer[2]=='\0'){ // jika buffer = 90
		  PWM = 900; // set value PWM = 90 to duty cycle 90%
	  }
	  else if(buffer[0]=='1' && buffer[1]=='0' && buffer[2]=='0' && buffer[3]=='\0'){ // jika buffer = 100
		  PWM = 1000; // set value PWM = 1000 to duty cycle 100%
	  }

	  __HAL_TIM_SET_COMPARE(&htim14,TIM_CHANNEL_1, PWM);	//set compare TIM dengan PWM
    osDelay(1); // delay 1ms
    memset(Rx_data, '\0', 64);	// set clear Rx_data
  }
  /* USER CODE END StartPWM */
}

